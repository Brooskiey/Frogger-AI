package neat;

/**
 * Enumeration to represent the layers a node can exist in.
 * @author Chance Simmons and Brandon Townsend
 * @version 18 January 2020
 */
public enum NodeLayer {
    HIDDEN,             // Value for hidden layer.
    INPUT,              // Value for input layer.
    OUTPUT,             // Value for output layer.
    BIAS                // Value for bias layer.
}
